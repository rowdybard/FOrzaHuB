// Helper: turn a sorted list of [user, value, proofType, agoHours, verified?] into ranked entries
function board(rows) {
  return rows.map(([user, value, proof, ago, verified = true], i) => ({
    rank: i + 1,
    user,
    value,
    proof: { type: proof, url: '#' },
    submittedAt: h(-ago),
    verified,
  }))
}

function gallery(rows) {
  return rows.map(([user, title, votes, hue, ago], i) => ({
    id: `g${i}`,
    rank: i + 1,
    user,
    title,
    votes,
    hue,
    submittedAt: h(-ago),
    verified: true,
  }))
}

